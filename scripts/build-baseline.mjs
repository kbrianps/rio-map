#!/usr/bin/env node
import sharp from 'sharp';
import { writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, resolve } from 'node:path';

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUT = resolve(__dirname, '..', 'src', 'baseline.ts');

const RIO_CENTER_LAT = -22.9083;
const RIO_CENTER_LNG = -43.1964;
const ZOOM = 10;
const TILE_SIZE = 256;
const TILES_X = 3;
const TILES_Y = 3;
const SUBDOMAINS = 'abcd';
const URL_TEMPLATE = 'https://{s}.basemaps.cartocdn.com/light_all/{z}/{x}/{y}.png';

function lngToTileX(lng, z) {
  return Math.floor(((lng + 180) / 360) * Math.pow(2, z));
}
function latToTileY(lat, z) {
  const r = (lat * Math.PI) / 180;
  return Math.floor(
    ((1 - Math.log(Math.tan(r) + 1 / Math.cos(r)) / Math.PI) / 2) * Math.pow(2, z),
  );
}
function tileToLng(x, z) {
  return (x / Math.pow(2, z)) * 360 - 180;
}
function tileToLat(y, z) {
  const n = Math.PI - (2 * Math.PI * y) / Math.pow(2, z);
  return (180 / Math.PI) * Math.atan(0.5 * (Math.exp(n) - Math.exp(-n)));
}

async function fetchTile(z, x, y) {
  const s = SUBDOMAINS[(x + y) % SUBDOMAINS.length];
  const url = URL_TEMPLATE
    .replace('{s}', s)
    .replace('{z}', z)
    .replace('{x}', x)
    .replace('{y}', y);
  const res = await fetch(url, {
    headers: { 'User-Agent': 'rio-map-baseline-builder/0.1' },
  });
  if (!res.ok) throw new Error(`tile ${z}/${x}/${y}: HTTP ${res.status}`);
  return Buffer.from(await res.arrayBuffer());
}

async function main() {
  const cx = lngToTileX(RIO_CENTER_LNG, ZOOM);
  const cy = latToTileY(RIO_CENTER_LAT, ZOOM);
  const startX = cx - Math.floor(TILES_X / 2);
  const startY = cy - Math.floor(TILES_Y / 2);

  console.log(
    `[baseline] zoom=${ZOOM} grid=${TILES_X}x${TILES_Y} starting at tile (${startX},${startY})`,
  );

  const composite = [];
  for (let dy = 0; dy < TILES_Y; dy++) {
    for (let dx = 0; dx < TILES_X; dx++) {
      const tx = startX + dx;
      const ty = startY + dy;
      const buf = await fetchTile(ZOOM, tx, ty);
      composite.push({ input: buf, top: dy * TILE_SIZE, left: dx * TILE_SIZE });
    }
  }

  const stitched = await sharp({
    create: {
      width: TILES_X * TILE_SIZE,
      height: TILES_Y * TILE_SIZE,
      channels: 3,
      background: { r: 248, g: 250, b: 252 },
    },
  })
    .composite(composite)
    .jpeg({ quality: 70, progressive: true, mozjpeg: true })
    .toBuffer();

  const resized = await sharp(stitched)
    .resize({ width: 512, fit: 'inside' })
    .jpeg({ quality: 65, progressive: true, mozjpeg: true })
    .toBuffer();

  const base64 = resized.toString('base64');
  const dataUri = `data:image/jpeg;base64,${base64}`;

  const minLng = tileToLng(startX, ZOOM);
  const maxLng = tileToLng(startX + TILES_X, ZOOM);
  const maxLat = tileToLat(startY, ZOOM);
  const minLat = tileToLat(startY + TILES_Y, ZOOM);

  const ts = `// Auto-generated by scripts/build-baseline.mjs. Do not edit by hand.
// Pre-rendered preview of Rio at zoom ${ZOOM}, used as instant background while CartoCDN tiles load.
// Encoded as a JPEG data URI (~${(resized.length / 1024).toFixed(1)} KB).

export const BASELINE_DATA_URI =
  '${dataUri}';

export const BASELINE_BOUNDS = {
  minLat: ${minLat},
  minLng: ${minLng},
  maxLat: ${maxLat},
  maxLng: ${maxLng},
};
`;

  writeFileSync(OUT, ts);
  console.log(
    `[baseline] wrote ${OUT} (image ${resized.length} bytes / data uri ${dataUri.length} chars)`,
  );
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
